    private void mostrarRegistrosGuardados(String pFecha_incial , String pFecha_final){
        
        DefaultTableModel tabla = new DefaultTableModel();
        
        ArrayList<ObjetosProforma> listaProformas = new ArrayList();
        
        //VERIFICAR ALGUN ERROR AL CARGAR LA INFORMACION DE LAS PROFORMAS
        try{
            listaProformas = NuevaProforma.seleccionarProformaFecha(pFecha_incial, pFecha_final);
            System.out.println("SE CONSULTARON LOS DATOS DE LAS PROFORMAS");
        }catch(Error error){
            Mensaje.manipulacionExcepciones("critico", "Ocurrio un error al consultar la informacion " + error);
        }
        
        //MOSTRAR LA INFORMACION DE LAS PROFORMAS EN LA VISTA
        if(listaProformas.isEmpty()){
            Mensaje.manipulacionExcepciones("critico", "No existen datos almacenados de las proformas");
        } else {
            tabla.addColumn("ID Proforma");
            tabla.addColumn("ID Cliente");
            tabla.addColumn("ID Empleado");
            tabla.addColumn("Fecha");
            tabla.addColumn("Direccion");
            tabla.addColumn("Total");
            tabla.setRowCount(listaProformas.size());
            int cProforma = 0;
            
            for (ObjetosProforma xFactura : listaProformas) {
                tabla.setValueAt(xFactura.getId_proforma(), cProforma, 0);
                tabla.setValueAt(xFactura.getId_cliente(), cProforma, 1);
                tabla.setValueAt(xFactura.getId_empleado(), cProforma, 2);
                tabla.setValueAt(xFactura.getFecha_emision_proforma(), cProforma, 3);
                tabla.setValueAt(xFactura.getDir_envio_proforma(), cProforma, 4);
                tabla.setValueAt(xFactura.getTotal_proforma(), cProforma, 5);
                cProforma++;
            }
            
            tblDetalleVenta.setModel(tabla);
        }
    }
    
    

    private void cbxIdProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxIdProductoActionPerformed
        AccesoImagen acceso_imagen = new AccesoImagen();
        String producto = txtIdProducto.getText();
        int id = Integer.parseInt(cbxIdProducto.getSelectedItem().toString());
        Image imagen = acceso_imagen.buscar(id, producto);
        Dimension tamanio = lblImagen.getSize();
        Icon icono = new ImageIcon(imagen.getScaledInstance(tamanio.width, tamanio.height, BufferedImage.TYPE_INT_ARGB));
        lblImagen.setIcon(icono);
    }//GEN-LAST:event_cbxIdProductoActionPerformed

